import mongoose from "mongoose";

const cartSchema = new mongoose.Schema({
    userId:{
        type: mongoose.Schema.Types.ObjectId,
        ref: "Auth",
        required: true,
    },
    productId:{
        type: mongoose.Schema.Types.ObjectId,
        ref: "Product",
        required: true,
    },
    quantity:{
        type: Number, default: 1,
    }
},{timestamps: true});

export const Cart = mongoose.model("Cart",cartSchema);